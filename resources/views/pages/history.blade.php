                <h2 class="text-3xl font-bold text-purple-800 mb-6">BRIEF HISTORY OF THE SOCIETY</h2>

                <p class="mb-6">The journey to establishing OGUN STATE INSTITUTE OF TECHNOLOGY ACADEMIC STAFF IGBESA (ADO-ODO/OTA) COOPERATIVE MULTIPURPOSE SOCIETY LIMITED, formerly known as OGITECH ACADEMIC STAFF COOPERATIVE MULTIPURPOSE SOCIETY, started with a motion raised by Comr. Eletu, Ahmed Ajiboye on the floor of the ASUP, OGITECH Chapter congress held in November 2021 but did not get the needed attention as the congress was in a rush to close for the day. The motion, however, was interesting to the then General Secretary, Comr. Gafar Oluwasegun Quadri, who met with Comr. Eletu, Ahmed Ajiboye, to deliberate further on the possibility.</p>

                <p class="mb-6">On Thursday, 17th March 2022, the ASUP Cooperative Committee was set up by Comr. Gafar, Oluwasegun Quadri led executives immediately after they were sworn into office at the congress of ASUP, OGITECH Chapter. The committee began by first considering the prevailing situation surrounding the existing cooperative societies in the Institute highlighting both good and unethical practices in their custom over the years with a view of establishing a Society with a difference and welfare of members at heart. The committee threw its weight behind the newly proposed academic staff cooperative society with a resolution to commence a competitive society with global standards.</p>


                <p class="mb-6">The committee considered the proposed bye-laws by Comr. Eletu, Ahmed Ajiboye, querying grey areas, restructured where necessary and added to its content, then adopted it. An effort was made to reach out to officials of the Ministry of Community Development and Cooperatives and the Ogun State Cooperative Federation Limited (OGSCOFED) to harvest current and detailed information leading to possible formalization of the proposed society.</p>


                <p class="mb-6">Considering the peculiarity of our dear Institute, our environment and the lessons learnt from existing cooperative societies, the committee then appealed to ASUP, OGITECH Chapter for sponsorship insisting on an independent and autonomous cooperative society devoid of favoritism and sharp practices.</p>

                <p class="mb-6">On completion of its assignment, ASUP, OGITECH Chapter credited the committee with N47,000 (N15,800 committee assignment expenses and N31,200 donations for procurement of pre-registration journals and opening balance required for opening the society’s bank account). ASUP OGITECH Chapter's Chairman, Comr. Gafar Oluwasegun Quadri then charged the committee to kickstart the society as its pro-tem management committee to harvest required documents, register the society with relevant government agencies, develop its policies and establish templates for its success.</p>


                <p class="mb-6">5th April 2022 signifies the starting date of the society now known as the <b>OGUN STATE INSTITUTE OF TECHNOLOGY ACADEMIC STAFF IGBESA (ADO-ODO/OTA) COOPERATIVE MULTIPURPOSE SOCIETY LIMITED.</b><br />
                    Our Society is now fully registered (No.: 14080) under Section 7(1) of the Cooperative Societies Law, Cap. 26 of the Ogun State of Nigeria Laws, Vol. 1 of 2006.
                </p>



